dict = {1: "he", 2: "she", 3: "I"}
for i in dict:
    print(i)
print("----------------------------------------")
dict1 = {1: "he", 2: "she", 3: "I"}
for i in dict1.items():
    print(i)
print("----------------------------------------")
for i in dict1.keys():
    print(i)
print("----------------------------------------")
for i in dict1.values():
    print(i)
